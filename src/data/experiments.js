export const getExperimentsBySubject = (subject) => {
  return experiments.filter(exp => exp.subject === subject);
};

export const getWAECRecommended = () => {
  return experiments.filter(exp => exp.waecRelevant);
};

export const getExperimentsByDifficulty = (difficulty) => {
  return experiments.filter(exp => exp.difficulty === difficulty);
};

export const getExperimentsByCategory = (category) => {
  return experiments.filter(exp => exp.category === category);
};

export const searchExperiments = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return experiments.filter(exp => 
    exp.title.toLowerCase().includes(lowercaseQuery) ||
    exp.subject.toLowerCase().includes(lowercaseQuery) ||
    exp.topic.toLowerCase().includes(lowercaseQuery) ||
    exp.description.toLowerCase().includes(lowercaseQuery)
  );
};